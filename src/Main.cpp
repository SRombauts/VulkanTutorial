#include <iostream>
#include <stdexcept>
#include <cstdlib>

#include "HelloTriangleApplication.h"

/**
 * Entry point of the application
 * 
 * @return 0
 */
int main() {
    HelloTriangleApplication app;

    try {
        app.run();
    }
    catch (const std::runtime_error& e) {
        std::cerr << e.what() << std::endl;
        return EXIT_FAILURE;
    }

    return EXIT_SUCCESS;
}
